/**
 * GET /reports/export/pdf?type=...&period=...&startDate=...&endDate=...
 *
 * Streams a fully branded PDF:
 *  • Cover page  : Anchor Hotel Suite logo block, report title, period, generated-by auth stamp
 *  • Data pages  : Tables with navy headers, alternating row shading
 *  • Every page footer: "Powered by Anchor Hotel Suite" + page number
 */
router.get('/export/pdf', authenticate, requireLevel(7), async (req, res) => {
  try {
    const { type = 'financial', hotelId, period, startDate, endDate, year, groupBy = 'day' } = req.query;
    const hotelIds = await resolveHotelScope(req.user, hotelId);
    const groupId  = resolveGroupId(req.user, req.query.groupId);
    const { start, end } = resolvePeriod(period, startDate, endDate);

    // ── Collect data based on type ────────────────────────────────────────────
    let reportData = null;
    let reportTitle = '';

    if (type === 'financial') {
      reportTitle = 'Financial Report';
      const where = { hotelId: { in: hotelIds }, createdAt: { gte: start, lte: end } };
      const txns = await prisma.transaction.findMany({
        where, include: { hotel: { select: { name: true } } }, orderBy: { createdAt: 'asc' },
      });
      const income  = txns.filter((t) => t.type === 'INCOME').reduce((s, t) => s + Number(t.amount), 0);
      const expense = txns.filter((t) => t.type === 'EXPENSE').reduce((s, t) => s + Number(t.amount), 0);
      reportData = { type, txns, income, expense };
    }

    else if (type === 'group-performance' || type === 'group-hotel-compare') {
      reportTitle = type === 'group-performance' ? 'Group Performance Report' : 'Hotel Comparison Report';
      const hotels = await prisma.hotel.findMany({
        where: { ...(groupId ? { groupId } : { id: { in: hotelIds } }), isActive: true },
        select: { id: true, name: true, totalRooms: true },
        orderBy: { name: 'asc' },
      });
      const hIds = hotels.map((h) => h.id);
      const dr   = { gte: start, lte: end };
      const [rev, exps, ci, nights, posRev] = await Promise.all([
        prisma.transaction.groupBy({ by: ['hotelId'], where: { hotelId: { in: hIds }, type: 'INCOME',  createdAt: dr }, _sum: { amount: true } }),
        prisma.transaction.groupBy({ by: ['hotelId'], where: { hotelId: { in: hIds }, type: 'EXPENSE', createdAt: dr }, _sum: { amount: true } }),
        prisma.roomLog.groupBy({ by: ['hotelId'], where: { hotelId: { in: hIds }, status: { not: 'CANCELLED' }, createdAt: dr }, _count: true }),
        prisma.roomLog.groupBy({ by: ['hotelId'], where: { hotelId: { in: hIds }, status: { not: 'CANCELLED' }, createdAt: dr }, _sum: { nights: true } }),
        prisma.pOSSale.groupBy({ by: ['hotelId'], where: { hotelId: { in: hIds }, createdAt: dr }, _sum: { totalAmount: true } }),
      ]);
      const ri = Object.fromEntries(rev.map((r) => [r.hotelId, Number(r._sum?.amount || 0)]));
      const ei = Object.fromEntries(exps.map((r) => [r.hotelId, Number(r._sum?.amount || 0)]));
      const cii= Object.fromEntries(ci.map((r) => [r.hotelId, r._count]));
      const ni = Object.fromEntries(nights.map((r) => [r.hotelId, Number(r._sum?.nights || 0)]));
      const pi = Object.fromEntries(posRev.map((r) => [r.hotelId, Number(r._sum?.totalAmount || 0)]));
      const hotelStats = hotels.map((h) => {
        const revenue = ri[h.id] || 0;
        const expense = ei[h.id] || 0;
        const nightsCount = ni[h.id] || 0;
        const occ = h.totalRooms > 0 && nightsCount > 0 ? Math.min(100, Math.round((nightsCount / (h.totalRooms * 30)) * 100)) : 0;
        return { name: h.name, totalRooms: h.totalRooms, revenue, expense, profit: revenue - expense,
                 margin: revenue > 0 ? Math.round(((revenue - expense) / revenue) * 100) : 0,
                 checkIns: cii[h.id] || 0, nights: nightsCount, occupancyRate: occ, pos: pi[h.id] || 0 };
      });
      const groupTotals = hotelStats.reduce((a, h) => ({
        revenue: a.revenue + h.revenue, expense: a.expense + h.expense,
        profit: a.profit + h.profit, checkIns: a.checkIns + h.checkIns, pos: a.pos + h.pos,
      }), { revenue: 0, expense: 0, profit: 0, checkIns: 0, pos: 0 });
      reportData = { type, hotels: hotelStats, groupTotals };
    }

    else if (type === 'occupancy') {
      reportTitle = 'Occupancy Report';
      const logs = await prisma.roomLog.findMany({
        where: { hotelId: { in: hotelIds }, status: { not: 'CANCELLED' }, createdAt: { gte: start, lte: end } },
        include: { room: { select: { number: true, type: true } }, hotel: { select: { name: true } } },
        orderBy: { checkInDate: 'asc' },
      });
      reportData = { type, logs };
    }

    else if (type === 'pos-summary') {
      reportTitle = 'POS Sales Report';
      const sales = await prisma.pOSSale.findMany({
        where: { hotelId: { in: hotelIds }, createdAt: { gte: start, lte: end } },
        include: { items: true, hotel: { select: { name: true } }, staff: { select: { name: true } } },
        orderBy: { createdAt: 'asc' },
      });
      reportData = { type, sales };
    }

    else {
      return res.status(400).json({ error: `PDF export not supported for type: ${type}. Use: financial, occupancy, pos-summary, group-performance, group-hotel-compare` });
    }

    // ── Build PDF ─────────────────────────────────────────────────────────────
    const safeType = type.replace(/[^a-z0-9-]/gi, '-');
    const filename = `anchor-${safeType}-${start.toISOString().slice(0, 10)}_to_${end.toISOString().slice(0, 10)}.pdf`;
    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename="${filename}"`);

    const doc = buildReportPDF({ reportTitle, reportData, start, end, user: req.user, groupId, type });
    doc.pipe(res);
    doc.end();
  } catch (err) {
    console.error('PDF export error:', err);
    res.status(500).json({ error: 'Failed to export PDF report' });
  }
});

