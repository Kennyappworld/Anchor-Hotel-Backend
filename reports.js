/**
 * GET /reports/group/performance
 * Top-level KPI dashboard for a group: revenue, occupancy, POS, expenses
 * broken down per hotel subsidiary, with group-wide totals.
 *
 * Query: ?groupId=&period=&startDate=&endDate=
 */
router.get('/group/performance', authenticate, requireLevel(8), async (req, res) => {
  try {
    const { period, startDate, endDate } = req.query;
    const groupId  = resolveGroupId(req.user, req.query.groupId);
    const { start, end } = resolvePeriod(period, startDate, endDate);

    // Fetch all hotels in this group
    const hotels = await prisma.hotel.findMany({
      where: { ...(groupId ? { groupId } : {}), isActive: true },
      select: { id: true, name: true, currency: true, _count: { select: { rooms: true, users: true } } },
      orderBy: { name: 'asc' },
    });

    const hotelIds = hotels.map((h) => h.id);
    const dateRange = { gte: start, lte: end };

    // Parallel fetch across all hotels
    const [
      incomeAgg, expenseAgg,
      checkIns, checkOuts,
      nightsAgg, posAgg,
      maintenanceOpen, activeStaff,
    ] = await Promise.all([
      prisma.transaction.groupBy({
        by: ['hotelId'],
        where: { hotelId: { in: hotelIds }, type: 'INCOME',  createdAt: dateRange },
        _sum: { amount: true },
      }),
      prisma.transaction.groupBy({
        by: ['hotelId'],
        where: { hotelId: { in: hotelIds }, type: 'EXPENSE', createdAt: dateRange },
        _sum: { amount: true },
      }),
      prisma.roomLog.groupBy({
        by: ['hotelId'],
        where: { hotelId: { in: hotelIds }, status: { not: 'CANCELLED' }, createdAt: dateRange },
        _count: true,
      }),
      prisma.roomLog.groupBy({
        by: ['hotelId'],
        where: { hotelId: { in: hotelIds }, status: 'CHECKED_OUT', createdAt: dateRange },
        _count: true,
      }),
      prisma.roomLog.groupBy({
        by: ['hotelId'],
        where: { hotelId: { in: hotelIds }, status: { not: 'CANCELLED' }, createdAt: dateRange },
        _sum: { nights: true },
      }),
      prisma.pOSSale.groupBy({
        by: ['hotelId'],
        where: { hotelId: { in: hotelIds }, createdAt: dateRange },
        _sum: { totalAmount: true },
        _count: true,
      }),
      prisma.maintenanceLog.groupBy({
        by: ['hotelId'],
        where: { hotelId: { in: hotelIds }, status: { in: ['OPEN', 'IN_PROGRESS'] } },
        _count: true,
      }),
      prisma.user.groupBy({
        by: ['hotelId'],
        where: { hotelId: { in: hotelIds }, isVerified: true },
        _count: true,
      }),
    ]);

    // Index by hotelId for O(1) lookup
    const idx = (arr, key = 'hotelId') => Object.fromEntries(arr.map((r) => [r[key], r]));
    const incIdx  = idx(incomeAgg);
    const expIdx  = idx(expenseAgg);
    const ciIdx   = idx(checkIns);
    const coIdx   = idx(checkOuts);
    const nIdx    = idx(nightsAgg);
    const posIdx  = idx(posAgg);
    const maintIdx= idx(maintenanceOpen);
    const staffIdx= idx(activeStaff);

    const hotelStats = hotels.map((h) => {
      const income      = Number(incIdx[h.id]?._sum?.amount  || 0);
      const expense     = Number(expIdx[h.id]?._sum?.amount  || 0);
      const posRevenue  = Number(posIdx[h.id]?._sum?.totalAmount || 0);
      const totalNights = Number(nIdx[h.id]?._sum?.nights   || 0);
      const checkInCount= ciIdx[h.id]?._count || 0;
      const checkOutCount=coIdx[h.id]?._count || 0;
      const totalRooms  = h._count.rooms;
      const occupancyRate = totalRooms > 0 && totalNights > 0
        ? Math.min(100, Math.round((totalNights / (totalRooms * 30)) * 100)) : 0;

      return {
        hotelId:       h.id,
        hotelName:     h.name,
        totalRooms,
        staffCount:    staffIdx[h.id]?._count || 0,
        revenue: {
          accommodation: income - posRevenue,
          pos:           posRevenue,
          total:         income,
        },
        expenses:      expense,
        netProfit:     income - expense,
        occupancy: {
          checkIns:   checkInCount,
          checkOuts:  checkOutCount,
          totalNights,
          occupancyRate,
        },
        pos: {
          totalRevenue:    posRevenue,
          transactionCount: posIdx[h.id]?._count || 0,
        },
        openMaintenanceIssues: maintIdx[h.id]?._count || 0,
      };
    });

    // Group-wide totals
    const groupTotals = hotelStats.reduce(
      (acc, h) => ({
        totalRevenue:    acc.totalRevenue    + h.revenue.total,
        totalExpenses:   acc.totalExpenses   + h.expenses,
        netProfit:       acc.netProfit       + h.netProfit,
        totalCheckIns:   acc.totalCheckIns   + h.occupancy.checkIns,
        totalNights:     acc.totalNights     + h.occupancy.totalNights,
        totalPOSRevenue: acc.totalPOSRevenue + h.pos.totalRevenue,
        openMaintenance: acc.openMaintenance + h.openMaintenanceIssues,
      }),
      { totalRevenue: 0, totalExpenses: 0, netProfit: 0,
        totalCheckIns: 0, totalNights: 0, totalPOSRevenue: 0, openMaintenance: 0 }
    );

    // Best & worst performers
    const sorted      = [...hotelStats].sort((a, b) => b.revenue.total - a.revenue.total);
    const topHotel    = sorted[0]   || null;
    const bottomHotel = sorted[sorted.length - 1] || null;

    res.json({
      period: { start, end },
      groupId,
      groupTotals,
      hotels: hotelStats,
      insights: {
        topPerformer:    topHotel    ? { id: topHotel.hotelId,    name: topHotel.hotelName,    revenue: topHotel.revenue.total }    : null,
        bottomPerformer: bottomHotel ? { id: bottomHotel.hotelId, name: bottomHotel.hotelName, revenue: bottomHotel.revenue.total } : null,
        avgOccupancyRate: hotelStats.length
          ? Math.round(hotelStats.reduce((s, h) => s + h.occupancy.occupancyRate, 0) / hotelStats.length) : 0,
      },
    });
  } catch (err) {
    console.error('Group performance error:', err);
    res.status(500).json({ error: 'Failed to generate group performance report' });
  }
});

/**
 * GET /reports/group/hotel-compare
 * Side-by-side comparison table of key metrics across all hotels in a group.
 * Useful for spotting outliers and benchmarking.
 */
router.get('/group/hotel-compare', authenticate, requireLevel(8), async (req, res) => {
  try {
    const { period, startDate, endDate } = req.query;
    const groupId  = resolveGroupId(req.user, req.query.groupId);
    const { start, end } = resolvePeriod(period, startDate, endDate);

    const hotels  = await prisma.hotel.findMany({
      where: { ...(groupId ? { groupId } : {}), isActive: true },
      select: { id: true, name: true, totalRooms: true, maxStaff: true, vatPercent: true },
      orderBy: { name: 'asc' },
    });
    const hotelIds = hotels.map((h) => h.id);
    const dateRange = { gte: start, lte: end };

    const [revenue, costs, bookings, posRev, expenses] = await Promise.all([
      prisma.transaction.groupBy({
        by: ['hotelId'],
        where: { hotelId: { in: hotelIds }, type: 'INCOME', createdAt: dateRange },
        _sum: { amount: true },
      }),
      prisma.transaction.groupBy({
        by: ['hotelId'],
        where: { hotelId: { in: hotelIds }, type: 'EXPENSE', createdAt: dateRange },
        _sum: { amount: true },
      }),
      prisma.roomLog.groupBy({
        by: ['hotelId'],
        where: { hotelId: { in: hotelIds }, status: { not: 'CANCELLED' }, createdAt: dateRange },
        _count: true,
        _avg:  { totalAmount: true, nights: true },
        _sum:  { nights: true },
      }),
      prisma.pOSSale.groupBy({
        by: ['hotelId'],
        where: { hotelId: { in: hotelIds }, createdAt: dateRange },
        _sum: { totalAmount: true },
        _count: true,
      }),
      prisma.expense.groupBy({
        by: ['hotelId'],
        where: { hotelId: { in: hotelIds }, status: 'APPROVED', createdAt: dateRange },
        _sum: { amount: true },
        _count: true,
      }),
    ]);

    const ri = Object.fromEntries(revenue.map((r) => [r.hotelId, r]));
    const ci = Object.fromEntries(costs.map((r) => [r.hotelId, r]));
    const bi = Object.fromEntries(bookings.map((r) => [r.hotelId, r]));
    const pi = Object.fromEntries(posRev.map((r) => [r.hotelId, r]));
    const ei = Object.fromEntries(expenses.map((r) => [r.hotelId, r]));

    const comparison = hotels.map((h) => {
      const rev      = Number(ri[h.id]?._sum?.amount || 0);
      const cost     = Number(ci[h.id]?._sum?.amount || 0);
      const nights   = Number(bi[h.id]?._sum?.nights || 0);
      const bookingCount = bi[h.id]?._count || 0;
      const pos      = Number(pi[h.id]?._sum?.totalAmount || 0);

      return {
        hotelId:           h.id,
        hotelName:         h.name,
        totalRooms:        h.totalRooms,
        vatPercent:        Number(h.vatPercent),
        totalRevenue:      rev,
        totalCosts:        cost,
        grossMargin:       rev > 0 ? Math.round(((rev - cost) / rev) * 100) : 0,
        netProfit:         rev - cost,
        bookings:          bookingCount,
        totalNights:       nights,
        avgStayNights:     bookingCount > 0 ? Math.round((nights / bookingCount) * 10) / 10 : 0,
        avgBookingValue:   bookingCount > 0 ? Math.round(Number(bi[h.id]?._avg?.totalAmount || 0)) : 0,
        occupancyRate:     h.totalRooms > 0 && nights > 0
                           ? Math.min(100, Math.round((nights / (h.totalRooms * 30)) * 100)) : 0,
        posRevenue:        pos,
        posSalesCount:     pi[h.id]?._count || 0,
        approvedExpenses:  Number(ei[h.id]?._sum?.amount || 0),
        expenseCount:      ei[h.id]?._count || 0,
        revPerRoom:        h.totalRooms > 0 ? Math.round(rev / h.totalRooms) : 0,
      };
    });

    // Rankings
    const ranked = {
      byRevenue:     [...comparison].sort((a, b) => b.totalRevenue - a.totalRevenue).map((h, i) => ({ rank: i + 1, hotelId: h.hotelId, value: h.totalRevenue })),
      byOccupancy:   [...comparison].sort((a, b) => b.occupancyRate - a.occupancyRate).map((h, i) => ({ rank: i + 1, hotelId: h.hotelId, value: h.occupancyRate })),
      byMargin:      [...comparison].sort((a, b) => b.grossMargin - a.grossMargin).map((h, i) => ({ rank: i + 1, hotelId: h.hotelId, value: h.grossMargin })),
    };

    res.json({ period: { start, end }, groupId, comparison, ranked });
  } catch (err) {
    console.error('Hotel compare error:', err);
    res.status(500).json({ error: 'Failed to generate hotel comparison' });
  }
});

/**
 * GET /reports/group/revenue-trend
 * Daily/weekly/monthly revenue trend for each hotel, aggregated over the group.
 */
router.get('/group/revenue-trend', authenticate, requireLevel(8), async (req, res) => {
  try {
    const { period, startDate, endDate, groupBy = 'day' } = req.query;
    const groupId  = resolveGroupId(req.user, req.query.groupId);
    const { start, end } = resolvePeriod(period, startDate, endDate);

    const hotels = await prisma.hotel.findMany({
      where: { ...(groupId ? { groupId } : {}), isActive: true },
      select: { id: true, name: true },
    });
    const hotelIds = hotels.map((h) => h.id);

    const transactions = await prisma.transaction.findMany({
      where: { hotelId: { in: hotelIds }, createdAt: { gte: start, lte: end } },
      select: { hotelId: true, type: true, amount: true, createdAt: true },
      orderBy: { createdAt: 'asc' },
    });

    // Build [{ date, hotelId, hotelName, income, expense, net }]
    const hotelMap = Object.fromEntries(hotels.map((h) => [h.id, h.name]));
    const trendMap = {};

    transactions.forEach((t) => {
      const key = `${formatDateKey(new Date(t.createdAt), groupBy)}__${t.hotelId}`;
      if (!trendMap[key]) trendMap[key] = { date: formatDateKey(new Date(t.createdAt), groupBy), hotelId: t.hotelId, hotelName: hotelMap[t.hotelId], income: 0, expense: 0, net: 0 };
      if (t.type === 'INCOME') trendMap[key].income += Number(t.amount);
      else                      trendMap[key].expense += Number(t.amount);
      trendMap[key].net = trendMap[key].income - trendMap[key].expense;
    });

    // Also compute group-level aggregate per date
    const groupTrend = {};
    Object.values(trendMap).forEach((row) => {
      if (!groupTrend[row.date]) groupTrend[row.date] = { date: row.date, income: 0, expense: 0, net: 0 };
      groupTrend[row.date].income  += row.income;
      groupTrend[row.date].expense += row.expense;
      groupTrend[row.date].net     += row.net;
    });

    res.json({
      period:     { start, end },
      groupBy,
      groupId,
      hotels:     hotels.map((h) => ({ id: h.id, name: h.name })),
      byHotel:    Object.values(trendMap).sort((a, b) => a.date.localeCompare(b.date)),
      groupTrend: Object.values(groupTrend).sort((a, b) => a.date.localeCompare(b.date)),
    });
  } catch (err) {
    console.error('Revenue trend error:', err);
    res.status(500).json({ error: 'Failed to generate revenue trend' });
  }
});

/**
 * GET /reports/group/staff-activity
 * Staff headcount, verified status, role distribution, and recent activity per hotel.
 */
router.get('/group/staff-activity', authenticate, requireLevel(8), async (req, res) => {
  try {
    const { period, startDate, endDate } = req.query;
    const groupId  = resolveGroupId(req.user, req.query.groupId);
    const { start, end } = resolvePeriod(period, startDate, endDate);

    const hotels = await prisma.hotel.findMany({
      where: { ...(groupId ? { groupId } : {}), isActive: true },
      select: { id: true, name: true, maxStaff: true },
      orderBy: { name: 'asc' },
    });
    const hotelIds = hotels.map((h) => h.id);
    const dateRange = { gte: start, lte: end };

    const [users, checkInActivity, posActivity, expenseActivity] = await Promise.all([
      prisma.user.findMany({
        where: { hotelId: { in: hotelIds } },
        select: { hotelId: true, role: true, isVerified: true, createdAt: true },
      }),
      prisma.roomLog.groupBy({
        by: ['hotelId', 'createdById'],
        where: { hotelId: { in: hotelIds }, createdAt: dateRange },
        _count: true,
      }),
      prisma.pOSSale.groupBy({
        by: ['hotelId', 'staffId'],
        where: { hotelId: { in: hotelIds }, createdAt: dateRange },
        _count: true,
        _sum: { totalAmount: true },
      }),
      prisma.expense.groupBy({
        by: ['hotelId'],
        where: { hotelId: { in: hotelIds }, createdAt: dateRange },
        _count: true,
      }),
    ]);

    const hotelStats = hotels.map((h) => {
      const hotelUsers  = users.filter((u) => u.hotelId === h.id);
      const byRole      = {};
      hotelUsers.forEach((u) => {
        if (!byRole[u.role]) byRole[u.role] = { total: 0, verified: 0 };
        byRole[u.role].total++;
        if (u.isVerified) byRole[u.role].verified++;
      });
      const checkIns  = checkInActivity.filter((c) => c.hotelId === h.id).reduce((s, c) => s + c._count, 0);
      const posSales  = posActivity.filter((p) => p.hotelId === h.id).reduce((s, p) => s + p._count, 0);
      const posRev    = posActivity.filter((p) => p.hotelId === h.id).reduce((s, p) => s + Number(p._sum?.totalAmount || 0), 0);
      const expenses  = expenseActivity.find((e) => e.hotelId === h.id)?._count || 0;

      return {
        hotelId:      h.id,
        hotelName:    h.name,
        maxStaff:     h.maxStaff,
        totalStaff:   hotelUsers.length,
        verifiedStaff:hotelUsers.filter((u) => u.isVerified).length,
        staffUtilisation: h.maxStaff > 0
          ? Math.round((hotelUsers.length / h.maxStaff) * 100) : 0,
        byRole,
        activity: {
          checkInsProcessed: checkIns,
          posSalesProcessed: posSales,
          posRevenue:        posRev,
          expensesSubmitted: expenses,
        },
      };
    });

    res.json({ period: { start, end }, groupId, hotels: hotelStats });
  } catch (err) {
    console.error('Staff activity error:', err);
    res.status(500).json({ error: 'Failed to generate staff activity report' });
  }
});

