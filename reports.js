/** Return the hotel IDs the requesting user is allowed to query. */
async function resolveHotelScope(user, queryHotelId) {
  if (user.role === 'SUPER_ADMIN') {
    if (queryHotelId) return [queryHotelId];
    const all = await prisma.hotel.findMany({ select: { id: true } });
    return all.map((h) => h.id);
  }
  if (user.role === 'GROUP_MANAGER') {
    const hotels = await prisma.hotel.findMany({
      where: { groupId: user.groupId, ...(queryHotelId ? { id: queryHotelId } : {}) },
      select: { id: true },
    });
    return hotels.map((h) => h.id);
  }
  // GENERAL_MANAGER and below
  return [user.hotelId];
}

/** Resolve the group ID the user belongs to. */
function resolveGroupId(user, queryGroupId) {
  if (user.role === 'SUPER_ADMIN') return queryGroupId || null;
  return user.groupId || null;
}

function resolvePeriod(period, startDate, endDate) {
  const now = new Date();
  if (startDate && endDate) {
    return {
      start: new Date(startDate + 'T00:00:00.000Z'),
      end:   new Date(endDate   + 'T23:59:59.999Z'),
    };
  }
  switch (period) {
    case 'today':
      return {
        start: new Date(Date.UTC(now.getUTCFullYear(), now.getUTCMonth(), now.getUTCDate())),
        end:   new Date(Date.UTC(now.getUTCFullYear(), now.getUTCMonth(), now.getUTCDate(), 23, 59, 59, 999)),
      };
    case 'week': {
      const d = new Date(now); d.setUTCDate(d.getUTCDate() - 7);
      return { start: d, end: now };
    }
    case 'year':
      return {
        start: new Date(Date.UTC(now.getUTCFullYear(), 0, 1)),
        end:   new Date(Date.UTC(now.getUTCFullYear(), 11, 31, 23, 59, 59, 999)),
      };
    case 'quarter': {
      const q = Math.floor(now.getUTCMonth() / 3);
      return {
        start: new Date(Date.UTC(now.getUTCFullYear(), q * 3, 1)),
        end:   new Date(Date.UTC(now.getUTCFullYear(), q * 3 + 3, 0, 23, 59, 59, 999)),
      };
    }
    case 'month':
    default:
      return {
        start: new Date(Date.UTC(now.getUTCFullYear(), now.getUTCMonth(), 1)),
        end:   new Date(Date.UTC(now.getUTCFullYear(), now.getUTCMonth() + 1, 0, 23, 59, 59, 999)),
      };
  }
}

function formatDateKey(date, groupBy) {
  const y = date.getUTCFullYear();
  const m = String(date.getUTCMonth() + 1).padStart(2, '0');
  const d = String(date.getUTCDate()).padStart(2, '0');
  if (groupBy === 'month') return `${y}-${m}`;
  if (groupBy === 'year')  return `${y}`;
  return `${y}-${m}-${d}`;
}

